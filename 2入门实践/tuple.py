'''
Python的元组与列表类似，不同之处在于元组的元素不能修改,
元组使用小括号,列表使用方括号,
元组创建很简单,只需要在括号中添加元素,并使用逗号隔开即可
'''
#元组实例化
t1 = tuple([7,8,9])
print(t1)
#字符串转换为元组
tstr = tuple('hello')
print(tstr)
#元组切片
print(tstr[0:2])
print(tstr[1:2])
#元组是不能修改的，这里提供变相修改的方法
t11 = list(t1)
t11[0] = 1
t3 = tuple(t11)
print(t3)
#元组合并
t4=(1,2)+(3,4)
print(t4)
